package operator;

public class PracticeEqualityRelationalOperator {
	
	// Equality Relational Operator
		//     == Equal to
		//    != not Equal to
		
		//  > grater
		
		//  >= grater or equal
		
		
		//   < less
		
		//   <= less or equal
	
	public static void main(String[]args) {
		int number1= 35;
		int number2= 45;
		
		if(number1== number2) {
			System.out.println("Yahoo");
			
		}else {
			System.out.println("Good Bye");
		}
		if(number1 != number2) {
			System.out.println("Afia");
		}else {
			System.out.println("Asif");
		}
		if(number1> number2) {
			System.out.println("Reem");
			
		}else {
			System.out.println("Noor");
		}
	}
	
	

}
