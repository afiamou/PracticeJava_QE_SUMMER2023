package basic;

public class PracticeConcatenation {
	
	//Concatenation
	//Connect String with variable using + plus sign
	//to add or combined the String with variable in one line
	
	public static void main(String[]args){
		
		String firstName="Afia";
		String lastName="Mou";
		String middleName="F";
		
		
		System.out.println("Student First Name: "+firstName);
		System.out.println("Student Last Name: "+lastName);
	
		System.out.println("Student Full Name: "+firstName + lastName);
		
		System.out.println("Student full name: "+firstName + middleName + lastName);
		
	
		
		
		
		
	}

}
